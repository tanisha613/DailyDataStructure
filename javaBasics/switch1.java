package javaBasics;

public class switch1 {
    public static void main(String[] args) {
        int number =2;
        switch(number){
            case 1:
                System.out.println("samosa");
                break;
             case 2:
             System.out.println("momos");
             break;
            case 3:
                System.out.println("tikki");
                break;
            default:
                System.out.println("none of these");
        }
    }
}
